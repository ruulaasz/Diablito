#pragma once
#include "omVector3D.h"

namespace OmicronSDK
{
	class omRay
	{
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Declaracion de contructores y destructor																				//
		/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public:
		explicit omRay(omVector3D& _origin = omVector3D(0.0f, 0.0f, 0.0f), omVector3D& _direction = omVector3D(0.0f, 0.0f, 0.0f)) : m_vecOrigin(_origin), m_vecDirection(_direction){}; /*!< Constructor Standard */
		omRay(const omRay& _ray);
		~omRay();

		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Declaracion de variables miembro de la clase																			//
		/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		omVector3D m_vecOrigin;		//<De donde viene el Rayo>//
		omVector3D m_vecDirection;	//<Hacia donde se dirige el rayo>//
		
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Declaracion de la Sobrecarga de operadores =,+,-,+=,-=																//
		/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		omRay& operator= (const omRay& _ray);
		omRay& operator+= (const omRay& _ray);
		omRay& operator-= (const omRay& _ray);

		omRay operator+ (const omRay& _ray);
		omRay operator- (const omRay& _ray);

		bool operator == (const omRay& _ray);
		bool operator != (const omRay& _ray);
	};
}
