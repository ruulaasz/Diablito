namespace OmicronSDK
{
	class omVector3D;

	class omMatrix3D
	{
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Declaracion de la estructura Matrix3x3																				//
		/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public:
		union
		{
			struct
			{
				float	m00, m01, m02,
						m10, m11, m12,
						m20, m21, m22;
			};

			float Line[3][3];
			float fMatrix[9];
		};

		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Declaracion de contructores y destructor																				//
		/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public:
		omMatrix3D();																																										
		omMatrix3D(const omMatrix3D& _CopyObj);																				

		~omMatrix3D();																																																		


		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Sobrecarga de Operadores																								//
		/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		omMatrix3D operator+	(const omMatrix3D& _Matrix3x3);
		omMatrix3D operator-	(const omMatrix3D& _Matrix3x3);
		omMatrix3D operator*	(const omMatrix3D& _Matrix3x3);
		omMatrix3D operator*	(float _Value);

		omMatrix3D& operator+=	(const omMatrix3D& _Matrix3x3);
		omMatrix3D& operator-=	(const omMatrix3D& _Matrix3x3);

		bool operator==			(const omMatrix3D& _Matrix3x3);
		bool operator!=			(const omMatrix3D& _Matrix3x3);
	};
}
